//Concatenar X Interpolar: 
//Vamos criar variaveis usando let, const e var, atribuir strings e vamos concatenar e interpolar.

var a = 'Abelha';
let b = 'Baleia';
const C = 'Cachorro';

//Interpolação
console.log(`A ${a} é amarela e tem listras pretas, já a ${b} é azul e o ${C} é fofo.`);

//Concatenação
console.log('A ' + a + ' faz zumbidos, ' + b + ' é grande, ' + C + ' é fofo.');